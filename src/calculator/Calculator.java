public class Calculator {

    public int sum(int num1, int num2) {
        return num1 + num2;
    }

    public int minus(int num1, int num2) {
        return num1 - num2;
    }

    public int division(int num1, int num2) {

        int result = num1 / num2;

        return result;
    }
}
